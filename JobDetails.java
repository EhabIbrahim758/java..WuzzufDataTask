

public class JobDetails {
    String  Title;
    String Company;
    String Location;
    String Type;
    String Level;
    String YearExp;
    String Country;
    String Skills;

    public JobDetails(String title, String company, String location, String type, String level, String yearExp, String country, String skills) {
        Title = title;
        Company = company;
        Location = location;
        Type = type;
        Level = level;
        YearExp = yearExp;
        Country = country;
        Skills = skills;
    }
}
